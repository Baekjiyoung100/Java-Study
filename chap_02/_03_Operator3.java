package chap_02;

public class _03_Operator3 {
    public static void main(String[] args) {
        // 비교 연산자, 참이면 true 거짓이면 false
        System.out.println(19 > 13); // 19는 13보다 크다 t
        System.out.println(3 > 100); // 3은 100보다 크다 f
        System.out.println(3 >= 3); // 3은 3보다 같거나 작다t
        System.out.println(5 <= 7); // 5는 7보다 작거나 같다 t

        System.out.println(3 == 5); // 3은 5와 같다 f
        System.out.println(3 != 5); // 3은 5와 같지 않다 t

    }
}
